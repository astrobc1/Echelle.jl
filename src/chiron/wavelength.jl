function get_λsolution_estimate_from_file(fname, order)
    f = FITSIO.FITS(fname)
    d = FITSIO.read(f[1])
    oi = (echelle_orders[2] - echelle_orders[1]) - (order - echelle_orders[1]) + 1
    λ = d[1, :, oi] ./ 10
    λ .-= gas_cell_shifts[order]
    return λ
end

